package com.example.jewelchase230.text;

/**
 * The position of renderable text above the grid.
 *
 * @author Will Kaye
 */
public enum TextPosition {
    /**
     * Show on the left side of the window.
     */
    Left,
    /**
     * Show in the center of the screen.
     */
    Middle,
    /**
     * Show on the right side of the window.
     */
    Right
}
